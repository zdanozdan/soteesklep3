var counterI = 0
function insFldX(parentOb, childOb)
{
  childOb.xID = 'X' + counterI;
  counterI--;
  return insFld(parentOb, childOb)
}
function insDocX(parentOb, childOb)
{
  childOb.xID = 'Y' + counterI;
  counterI--;
  return insDoc(parentOb, childOb)
}
